<br><h1> Crunch oblique à la poulie haute</h1><br>

<p align="left">
Le crunch à la poulie haute pour les obliques est essentiel pour le développement des abdominaux. Attention tout de même, comme le précise cet article pour avoir une taille fine, les lourdes charges sont à proscrire pour ne pas épaissir la taille. En revanche, pour tous les athlètes qui doivent tenir des charges lourdes au-dessus de la tête ont besoin d'abdominaux puissants afin d'assurer la stabilisation du centre corporel. Dans ce cas, n'hésitez pas à augmenter la charge pour développer un maximum de force. 

</p>

<h3> Execution</h3>
<p align="left">

    - Fixez la corde à l’extrémité du câble relié à la poulie haute, tenez-la fermement des deux mains et agenouillez-vous à environ 60 cm devant les contrepoids.<br>
    - Descendez les mains le long de la tête, près des oreilles, les coudes fléchis. Dans cette position de départ, le buste penché en avant avec les genoux et les hanches à environ 90° : le câble devrait être bien tendu.<br>
    - Expirez en tirant contre la résistance et faites une flexion du rachis en contractant les abdominaux. À mesure que vous arrondissez le dos, pivotez le buste pour amener un coude vers le genou opposé.<br>
    - En arrivant à la position basse, videz énergétiquement vos poumons et maintenez la position un instant en contractant les abdos et les obliques.
    - En inspirant, revenez lentement à la position de départ. Refaites l'exercice de l'autre côté.<br>
    - Pensez à garder la même position des mains et des coudes pendant tout l'exercice.<br>
    - Les hanches doivent rester à la verticale des genoux. Si l'on s'assied sur les mollets, on diminue l'amplitude du mouvement et les abdominaux ne sont plus en tension.

</p>
<br><br>

<h3> Principaux muscles sollicités</h3>
<p align="left">
 Le groupe des muscles de l'abdomen comprend le grand droit, le grand oblique et le petit oblique. Long et mince, le grand droit descend verticalement le long de la paroi abdominale; il naît sur les 5, 6 et 7 côtes sur l'appendice xiphoïde du sternum et se termine plus bas sur le pubis. Les moitiés droite et gauche sont séparées par la ligne blanche, long tendon coupé d'intersections tendineuses qui forment les rainures transversales généralement visibles quand l'abdomen est "écorché".<br>

Allant du grand droit jusqu'aux dorsaux, le grand oblique recouvre les côtés et l'avant de l'abdomen. Les fibres sont disposées en diagonale depuis leurs insertions inférieures des deux côtés de l'abdomen et forme la lettre "V". Le petit oblique est logé directement sous le grand oblique. Dans sa partie haute, ses fibres sont presque perpendiculaires à celles du grand obliques et forment un "V" à l'envers. Dans sa partie basse, près du pubis, les fibres sont presque horizontales. 
</p>
