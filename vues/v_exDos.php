<p align="left">
Pour travailler les muscles du dos, la traction est le mouvement le plus connu.<br>
Il existe aussi des exercices appelés "rowing" comme le rowing barre ou le rowing haltère et des exercices de tirage comme le tirage vertical.

<br>


<br><h1> Rowing avec deux haltères</h1><br>

<p align="left">
Moderniser un classique peut donner des résultats mitigés : ainsi, certains termes hybrides qu'on a inventés n'ont pas toujours eu le succès qui caractérise les modifications du rowing, mouvement de base du bodybuilding dans l'entraînement du dos. Le rowing buste penché avec haltères illustre bien un tel succès. Mais nous ne parlons pas ici de ce bon vieux tirage d'un bras avec une jambe sur le banc. Cette fois, il s'agit d'un tirage des deux bras, buste penché, avec rotation. 

</p>

<h3> Execution</h3>
<p align="left">

	- Les bras tendus et les coudes pointés vers l'extérieur, maintenez une paire d'haltères devant vos cuisses, les avant-bras en position neutre ou pronation (paume face à vous), les pieds juste à l'intérieur de la largeur des épaules, le menton un peu relevé et les genoux légèrement fléchis.<br>
    - Penchez-vous en avant à partir des hanches jusqu'à ce que votre buste soit légèrement au-dessus de l'horizontale, les bras perpendiculaires au sol et le tête relevée. Remarque : pour réduire les contraintes au niveau du dos, fléchissez vos genoux dans une position confortable.<br>
    - En maintenant la cambrure naturelle de votre colonne vertébrale, commencez le mouvement en serrant vos omoplates l'une contre l'autre et vers le bas.<br>
    - Dès que la rétractation des omoplates est complète, continuez à tirer les haltères en tournant vos paumes vers l'avant et en amenant les coudes contre le corps et en arrière. Ces mouvements devront être fluides et sans à-coups.<br>
    - Maintenez un instant la position en haut avant de redescendre lentement les charges en les contrôlant tout en inversant la rotation des mains.<br>
    - Inspirez entre les répétitions ou pendant la partie négative et expirez une fois que vous avez passé le point délicat du mouvement.<br>
    - Faites le nombre nécessaire de reps pour terminer votre série.


</p>
<br><br>

<h3> Principaux muscles sollicités</h3>
<p align="left">
Cet exercice sollicite les muscles grands dorsaux, le grand rond, les rhomboïdes et le faisceau moyen du trapèze.
